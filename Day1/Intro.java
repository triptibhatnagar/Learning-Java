/*
Java was originally developed by James Gosling at Sun Microsystems in 1995.
Features :-
1) Platform Independent/Portable - compiler converts source code to byte code and then the JVM executes the bytecode. This bytecode can run on any platform (Windows, Linux, macOS), i.e., if we compile a program on Windows, then we can execute it on Linux or macOS.
2) Object-Oriented Programming Language - Abstraction,Encapsulation,Inheritance,Polymorphism
3)Simple - no complex features like pointers
4)Reliable - puts efforts into checking errors that are not easy to detect by other prog lang by garbage collection, exception handling, memeory allocation
5)Multithreaded - run multiple tasks simultaneously
*/

